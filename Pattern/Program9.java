package JavaVibrant.Pattern;

public class Program9 {
    public static void main(String args[]) {
        int n = 69;
        for (int i = 65; i <= n; i++) {
            for (char j = 65; j <= n; j++) {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
